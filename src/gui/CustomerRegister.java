    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int r = jTable1.getSelectedRow();

            if (r == -1) {
                JOptionPane.showMessageDialog(this, "Please Select a Row", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                String email = jTable1.getValueAt(r, 1).toString();
                int option = JOptionPane.showConfirmDialog(this, "Do you want to Remove This Customer?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    MySQL.iud("UPDATE `customer` SET `status_id` = '2'  WHERE `email` = '" + email + "'");
                    loadUsers();
                    JOptionPane.showMessageDialog(this, "Customer Remove", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            }

        }
    }//GEN-LAST:event_jTable1MouseClicked

