    public void loadUsers() {
        try {

            ResultSet rs = MySQL.search("SELECT * FROM `customer` INNER JOIN `gender` ON `customer`.gender_id=`gender`.`id` INNER JOIN `country` ON \n"
                    + "`customer`.`country_id` = `country`.`id` \n"
                    + "INNER JOIN `status` ON `customer`.`status_id`=`status`.`id` WHERE `status`.`id`='1'\n"
                    + "ORDER BY customer.id  ASC ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("name"));
                v.add(rs.getString("email"));
                v.add(rs.getString("country.name"));
                v.add(rs.getString("mobile"));
                v.add(rs.getString("id_proof"));
                v.add(rs.getString("gender.name"));
                v.add(rs.getString("address"));
                dtm.addRow(v);

            }
            jTable1.setModel(dtm);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

