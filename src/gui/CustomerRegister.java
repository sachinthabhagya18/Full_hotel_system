    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id = 1;
        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String email = jTextField4.getText();
        String id_proof = jTextField5.getText();
        String address = jTextField6.getText();

        String country = jComboBox1.getSelectedItem().toString();
        String gender = jComboBox2.getSelectedItem().toString();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (country.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Country", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select Gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (id_proof.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter ID Proof", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs3 = MySQL.search("SELECT `id` FROM `country` WHERE `name` = '" + country + "'");
                rs3.next();

                ResultSet rs4 = MySQL.search("SELECT `id` FROM `gender` WHERE `name` = '" + gender + "'");
                rs4.next();

                String country_id = rs3.getString("id");
                String gender_id = rs4.getString("id");

                MySQL.iud("INSERT INTO `customer`(`name`,`email`,`mobile`,`id_proof`,`address`,`gender_id`,`country_id`)"
                        + "VALUES('" + name + "','" + email + "','" + mobile + "','" + id_proof + "','" + address + "'," + Integer.parseInt(gender_id) + "," + Integer.parseInt(country_id) + ")");

                JOptionPane.showMessageDialog(this, "Customer Registration Success", "success", JOptionPane.INFORMATION_MESSAGE);

                setVisible(true);
                new CustomerRegister().setVisible(true);
                this.dispose();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

