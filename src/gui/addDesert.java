    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String price = jTextField2.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Desert Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Desert Price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs = MySQL.search("SELECT * FROM `desert` WHERE `name`='" + name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Desert Already Exsist", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `desert`(`name`,`price`,`status_id`) VALUES ('" + name + "','" + price + "','1')");

                    loaddesert();
                    this.pr.loadDesert();
                    jTextField1.setText("");
                    jTextField2.setText("");
                    jTextField1.grabFocus();
                    JOptionPane.showMessageDialog(this, "New Desert Added", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int r = jTable1.getSelectedRow();

        if (r == -1) {
            JOptionPane.showMessageDialog(this, "Please Select a Row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String package_id = jTable1.getValueAt(r, 0).toString();
            String status = jTable1.getValueAt(r, 3).toString();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            try {
                int option = JOptionPane.showConfirmDialog(this, "Do you want to Status Update This Desert?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {

                    MySQL.iud("UPDATE `desert` SET `status_id` = '2'  WHERE `id` = '" + package_id + "'");

                    JOptionPane.showMessageDialog(this, "Desert Is Deactive", "Success", JOptionPane.INFORMATION_MESSAGE);
                    loaddesert();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
            this.pr.loadDesert();
            loaddesert();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

